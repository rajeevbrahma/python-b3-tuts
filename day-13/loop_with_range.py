# start - 0
# end - 10-1
# step - 1
for num in range(10):
    print (num)

# start - 10
# end - 100-1
# step - 10
for num in range(10,100,10):
    print (num)

# start - -50
# end - 50-1
# step - -10
for num in range(-50,50,10):
    print (num)

# start - 0
# end - 100
# step - -10
for num in range(100,0,-10):
    print (num)

