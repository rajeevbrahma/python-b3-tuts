# Integers 
# About Integers
# Integers are whole numbers
# Integers are immutable


a = 10
b = 20

print (a)
print (b)

# Floats
a = 10.5
b = 20.5

print (a)
print (b)

# Complex Numbers
a = 10 + 5j
b = 20 + 5j

print (a)
print (b)

# Strings
