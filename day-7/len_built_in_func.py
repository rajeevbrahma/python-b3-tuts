# built in function len 

# len() function is used to find the length of a string, list, tuple, dictionary, etc.

# syntax:
#         len(iterable) - iterable can be string, list, tuple, dictionary, etc.

# create a string
str1 = "Hello World"
print (len(str1)) # 11

# create a list
list1 = [1, 2, 3, 4, 5]
print (len(list1)) # 5


# create nested list
list2 = [1,2,[3,4,5,6]]
print (len(list2))
print (len(list2[2]))


