# string slicing 

# create a string
string1 = "Hello World"

# slice string elements from index 0 to 3
print(string1[0:3])  # Hel

# slice string elements from index 2 to 5
print(string1[2:5])  # llo

# slice string elements from index 0 to 5 with step 2
print(string1[0:5:2])  # Hlo

# slice string elements from index 0 to 5 with step 3
print(string1[0:5:3])  # Hl

# slice string elements from index 5 to 0 with step -1
print(string1[5:0:-1])  # olle


